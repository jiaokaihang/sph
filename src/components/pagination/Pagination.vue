<template>
  <div class="pagination">
    <button :disabled="pageNo == 1" @click="$emit('getPageNo', pageNo - 1)">
      上一页
    </button>
    <button
      v-if="startNumAndEndNum.start > 1"
      @click="$emit('getPageNo', 1)"
      :class="{ active: pageNo == 1 }"
    >
      1
    </button>

    <button v-if="startNumAndEndNum.start > 2">···</button>

    <button
      v-for="(page, index) in startNumAndEndNum.end"
      :key="index"
      v-show="page >= startNumAndEndNum.start"
      @click="$emit('getPageNo', pageNo)"
      :class="{ active: pageNo == page }"
    >
      {{ page }}
    </button>

    <button v-if="startNumAndEndNum.end < totalPage - 1">···</button>
    <button
      v-if="startNumAndEndNum.end < totalPage"
      @click="$emit('getPageNo', totalPage)"
      :class="{ active: pageNo == totalPage }"
    >
      {{ totalPage }}
    </button>
    <button
      :disabled="pageNo == totalPage"
      @click="$emit('getPageNo', pageNo + 1)"
    >
      下一页
    </button>

    <button style="margin-left: 30px">共{{ total }} 条</button>
  </div>
</template>

<script>
export default {
  name: "Pagination",
  props: ["pageNo", "pageSize", "total", "continues"],
  computed: {
    totalPage() {
      return Math.ceil(this.total / this.pageSize);
    },
    //计算连续页码的起始数字和结束的数字
    startNumAndEndNum() {
      const { continues, totalPage, pageNo } = this;
      //定义两个变量，存储起始数字和结束数字
      let start = 0,
        end = 0;
      //连续的页码数字如果出现不正常的现象，就是总的页数没有连续的页数多
      if (continues > totalPage) {
        start = 1;
        end = this.total;
      } else {
        //正常现象 总的页数大于连续的页数
        start = pageNo - parseInt(continues / 2); //起始数字
        end = pageNo + parseInt(continues / 2);
        //如果start是0或者负数的话，那么就给他纠正
        if (start < 1) {
          start = 1;
          end = continues; //结束数字就是你的页码数
        }
        //如果end大于当前总页码的话，那么就给他纠正
        if (end > totalPage) {
          end = totalPage; //结束页码就是最后的一页
          start = totalPage - continues + 1;
        }
      }
      return { start, end };
    },
  },
};
</script>

